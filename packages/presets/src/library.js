export const themeLibrary = Object.fromEntries(
  Object.entries(paletteMap).map(([name, palette]) => [name, buildPreset(name, palette)])
);

export function listThemeLibrary() {
  return Object.keys(themeLibrary);
}

export function getThemePreset(name = 'light') {
  return themeLibrary[name] || themeLibrary.light;
}

export function createThemePack(names = listThemeLibrary()) {
  return names.reduce((pack, name) => {
    pack[name] = getThemePreset(name);
    return pack;
  }, {});
}

